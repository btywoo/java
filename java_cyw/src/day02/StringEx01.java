package day02;

public class StringEx01 {

	public static void main(String[] args) {
		String str1 = null;
		String str2 = " ";
		String str3 = "집 가고 싶다";
		String str4 = "null";

		System.out.println(str1); // null 값이라는 의미 > 값이 저장되어 있지 않다
		System.out.println(str2);
		System.out.println(str3);
		System.out.println(str4); // "null"이라는 의미 > 'null'이라는 문자가 저장되어 있다

	}

}